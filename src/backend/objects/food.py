class Food:
    def __init__(
        self, 
        foodID, 
        brand, 
        name, 
        calories, 
        protein, 
        carbs, 
        fat
    ):
        self.foodID = foodID
        self.brand = brand
        self.name = name
        self.calories = calories
        self.protein = protein
        self.carbs = carbs
        self.fat = fat