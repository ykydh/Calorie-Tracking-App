class FoodLog:
    def __init__(
            self, 
            logID, 
            name,
            brand,
            weight, 
            calories, 
            protein, 
            carbs, 
            fat
        ):
        self.logID = logID
        self.name = name
        self.brand  = brand
        self.weight = weight
        self.calories = calories
        self.protein = protein
        self.carbs = carbs
        self.fat = fat